package admin.bean;

public class UserstatsDTO {
	private String id;
	private String year;
	private String month;
	private String day;
	private String stats;
	private String yemoda;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getStats() {
		return stats;
	}
	public void setStats(String stats) {
		this.stats = stats;
	}
	public String getYemoda() {
		return yemoda;
	}
	public void setYemoda(String yemoda) {
		this.yemoda = yemoda;
	}
	
}


